define(function () {
    return {
        add: function (p1, p2) {
            // 加法
            return p1 + p2
        },
        subtraction: function (p1, p2) {
            // 减法
            return p1 - p2
        },
        multiplication: function (p1, p2) {
            // 乘法
            return p1 * p2
        },
        division: function (p1, p2) {
            // 除法
            return p1 / p2
        },
        remainder: function (p1, p2) {
            // 取余
            return p1 % p2
        }
    }
})
